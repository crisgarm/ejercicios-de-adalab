"use strict";

function average(a, b, c, d) {
  const result = (a + b + c + d) / 4;
  return result;
}
const averageResult = average(5, 9, 6, 8);
console.log(averageResult);

const test02 = average(8, 19, 44, 32);
console.log(test02);

const test03 = average(0, 1, -4, 22);
console.log(test03);
